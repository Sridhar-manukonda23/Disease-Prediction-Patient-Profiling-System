
from io import BytesIO
from datetime import datetime
from reportlab.lib import colors
from reportlab.lib.enums import TA_CENTER
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.units import inch
from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle

def generate_pdf(report):
    buffer=BytesIO()
    doc=SimpleDocTemplate(buffer,pagesize=(8.27*inch,11.69*inch),
                          leftMargin=35,rightMargin=35,topMargin=35,bottomMargin=35)
    styles=getSampleStyleSheet()
    title=ParagraphStyle("Title",parent=styles["Title"],alignment=TA_CENTER,fontSize=22,textColor=colors.HexColor("#0d6efd"),spaceAfter=8)
    sub=ParagraphStyle("Sub",parent=styles["Heading2"],alignment=TA_CENTER,fontSize=11,textColor=colors.grey,spaceAfter=16)
    sec=ParagraphStyle("Sec",parent=styles["Heading2"],fontSize=14,textColor=colors.HexColor("#0d6efd"))
    body=styles["BodyText"]
    story=[]
    story.append(Paragraph("DISEASE PREDICTION REPORT",title))
    story.append(Paragraph("Disease Prediction & Patient Profiling System",sub))
    story.append(Paragraph("<b>AI Powered Healthcare Assistant</b>",sub))
    story.append(Spacer(1,10))
    story.append(Paragraph("PATIENT INFORMATION",sec))
    pt=Table([
      ["Patient Name",report.get("patient_name","")],
      ["Age",str(report.get("age",""))],
      ["Gender",report.get("gender","")],
      ["Symptoms",report.get("symptoms","").replace("_"," ").title()]
    ],colWidths=[140,320])
    pt.setStyle(TableStyle([
      ("BACKGROUND",(0,0),(0,-1),colors.HexColor("#EAF3FF")),
      ("GRID",(0,0),(-1,-1),0.5,colors.grey),
      ("BOX",(0,0),(-1,-1),0.8,colors.black),
      ("FONTNAME",(0,0),(0,-1),"Helvetica-Bold"),
      ("BOTTOMPADDING",(0,0),(-1,-1),8),
      ("TOPPADDING",(0,0),(-1,-1),8)
    ]))
    story.append(pt); story.append(Spacer(1,14))
    story.append(Paragraph("PREDICTION RESULT",sec))
    rt=Table([
      ["Predicted Disease",report.get("disease","")],
      ["Confidence",f"{report.get('confidence',0)} %"]
    ],colWidths=[170,290])
    rt.setStyle(TableStyle([
      ("BACKGROUND",(0,0),(0,-1),colors.HexColor("#DFF5E5")),
      ("GRID",(0,0),(-1,-1),0.5,colors.grey),
      ("BOX",(0,0),(-1,-1),0.8,colors.black),
      ("FONTNAME",(0,0),(0,-1),"Helvetica-Bold"),
      ("BOTTOMPADDING",(0,0),(-1,-1),8),
      ("TOPPADDING",(0,0),(-1,-1),8)
    ]))
    story.append(rt); story.append(Spacer(1,14))
    story.append(Paragraph("DISEASE DESCRIPTION",sec))
    story.append(Paragraph(report.get("description",""),body)); story.append(Spacer(1,14))
    story.append(Paragraph("RECOMMENDED PRECAUTIONS",sec))
    for p in report.get("precautions",[]): story.append(Paragraph("✓ "+str(p),body))
    story.append(Spacer(1,14))
    story.append(Paragraph("MEDICAL DISCLAIMER",sec))
    story.append(Paragraph("This report is generated using a Machine Learning model trained on a publicly available dataset. It is intended for educational purposes only and should not be considered a medical diagnosis. Please consult a qualified healthcare professional.",body))
    story.append(Spacer(1,14))
    story.append(Paragraph(f"<b>Generated On:</b> {datetime.now().strftime('%d-%b-%Y %I:%M %p')}",body))
    story.append(Paragraph("<b>Generated By:</b> Disease Prediction & Patient Profiling System",body))
    doc.build(story)
    buffer.seek(0)
    return buffer
